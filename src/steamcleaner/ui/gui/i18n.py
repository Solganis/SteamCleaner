LANGUAGES = {"en": "English", "ru": "Русский", "zh": "中文", "es": "Español", "pt-BR": "Português (Brasil)"}

_current_lang = "en"


def init_lang():
    global _current_lang
    saved = get_value("ui", "language")
    _current_lang = saved if saved in _TRANSLATIONS else "en"


def set_lang(lang: str):
    global _current_lang
    if lang in _TRANSLATIONS:
        _current_lang = lang
        save_value("ui", "language", lang)


def get_lang() -> str:
    return _current_lang


def t(key: str, **kwargs: str | int) -> str:
    text = _TRANSLATIONS.get(_current_lang, _TRANSLATIONS["en"]).get(key)
    if text is None:
        text = _TRANSLATIONS["en"].get(key, key)
    if kwargs:
        return text.format(**kwargs)
    return text


def t_category(category_value: str) -> str:
    return t(
        f"cat_{category_value}",
    )
